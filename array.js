// Array Method
var data = ['A' , 'B' , 'C' , 'D']
// push
// เพิ่มสมาชิกต่อท้าย array
data.push('E')
console.log(data)
// ['A' , 'B' , 'C' , 'D' , 'E']

// pop
// ลบสมาชิกตัวสุดท้ายออก
var x = data.pop()
console.log(data)
// ['A' , 'B' , 'C' , 'D']

// shift
// ลบสมาชิกตัวสุดแรกออก
var y = data.shift()
console.log(data)
// [B' , 'C' , 'D']

